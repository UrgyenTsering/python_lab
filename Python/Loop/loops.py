"""# **List Comprehension**"""

# generating list without comprehension
list_a = [1, 4, 7]
new_list_a = list()
for item in list_a:
    new_salary = item * 2
    new_list_a.append(new_salary)
print(list_a)
print(new_list_a)

# generating  list with Comprehension
new_list_with_com = [item * 2 for item in list_a]
print(new_list_with_com)

# generating  list with Comprehension with if condition
list_with_if = [item * 2 for item in list_a if item % 2 == 0]
print(list_with_if)

# generating  list with Comprehension with if  and else condition
new_list_a = [item * 2 if item % 2 == 0 else item + 10 for item in list_a]
print(new_list_a)


# Convert country names to uppercase using set comprehension
country_names = ["Nepal", "usa", "UK"]
country_upper = {country.upper() for country in country_names}
print(country_upper)
# {'NEPAL', 'UK', 'USA'}


"""# **Dictionary Comprehension**"""

# Create a dictionary with country names as keys and their lengths as values using dictionary comprehension
countries = ["Nepal", "usa", "UK"]
country_dict = {country: len(country) for country in countries}
print(country_dict)
# {'Nepal': 5, 'usa': 3, 'UK': 2}

# Increase employee salaries by 10% and uppercase employee IDs using dictionary comprehension
sample_dict = {"e1": 100, "e2": 200}
new_dict = {
    emp.upper(): round(1.1 * sal, 2) for emp, sal in sample_dict.items()
}  # increases a salary value (sal) by 10% and rounds the result to 2 decimal places
print(new_dict)

# Output: {'E1': 110.0, 'E2': 220.0}
