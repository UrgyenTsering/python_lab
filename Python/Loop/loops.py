# Break
user_list = ["Ram", "Shyam", "Hari", "Rabi", "Krishna"]
for user in user_list:
    print(user)
    if user == "Shyam":
        print("No need to iterate after Shyam")
        break

# Continue
user_list = ["Ram", "Shyam", "Hari", "Rabi", "Krishna"]
for user in user_list:
    if user == "Shyam":
        print("Shyam doesn't need evaluation.")
        continue
    print(user.upper())


