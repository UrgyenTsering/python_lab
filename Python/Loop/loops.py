# for loop in dictionary
dict_items = {
    "python": ["data science", "web-development", "automation"],
    "java": ["web-development", "desktop app"],
}
for key, value in dict_items.items():
    print(f"{key} = {value}")
    print(f"{key} is used for {', '.join(value)}")


"""
OUTPUT: 
    python = ['data science', 'web-development', 'automation']
    python is used for data science, web-development, automation
    java = ['web-development', 'desktop app']
    java is used for web-development, desktop app
"""

# 🔥 Key Concept:
# .items() → gives key + value
# join() → converts list → string
# ', '.join(value) → adds comma + space between items

"""# **While Loop**"""

# Print numbers from 1 to 10.
num = 1
while num <= 10:
    print(num)
    num = num + 1

# Print even numbers less 10
i = 0
while i < 10:
    if i % 2 == 0:
        print(i)
    i += 1

fruits = ["apple", "banana", "papaya"]
i = 0
while i < len(fruits):
    print(fruits[i])
    i += 1


