# Write a function get_product_remainder(num_1, num_2) that returns product and remainder of two numbers
def get_product_remainder(num_1, num_2):
  if(num_2==0):
    return "Error","Division By Zero"
  product_func=num_1*num_2
  remainder_func=num_1%num_2
  return product_func, remainder_func

product,remainder=get_product_remainder(12,5)
