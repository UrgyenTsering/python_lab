def get_data(l,r,x):
  num_list = [1, -3, 0, 2, 0, 7, 10, 8, -1]
  selected_data=num_list[l:r+1]
  zero_count=selected_data.count(0)
  total=sum(selected_data)
  total_with_replace=total+zero_count*x
  return total_with_replace
