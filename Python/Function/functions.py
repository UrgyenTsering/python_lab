# Using *args
def productor(*args):
    product = 1
    for item in args:
        product = product * item
    return product


print(productor(1))  # Output: 1
print(productor(2, 4))  # Output: 8
print(productor(3, 4, 5))  # Output: 60


# Using **kwargs
def display_info(**kwargs):
    print(kwargs)
    for key, value in kwargs.items():
        print(f"{key}: {value}")


display_info(name="John", age=25)  # output: { name: John , age:25 }

# Combining *args and operations
from functools import reduce


def perform_calculation(operation_name, *args):
    if operation_name.lower() == "sum":
        result = sum(args)
    elif operation_name.lower() == "product":
        result = reduce(lambda x, y: x * y, args)
    else:
        result = f"{operation_name} is not implemented"
    return result


sum_result = perform_calculation("sum", 1, 2, 4)
print(f"Total: {sum_result}")

product_result = perform_calculation("product", 1, 2, 4)
print(f"Product: {product_result}")

hcf_result = perform_calculation("hcf", 1, 2, 4)
print(f"HCF: {hcf_result}")


