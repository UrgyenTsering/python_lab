# Function that takes two numbers as arguments
# Returns both their product and division result
def productor(num_1, num_2):
    product = num_1 * num_2  # Multiply the two numbers
    div_n = num_1 / num_2  # Divide num_1 by num_2
    return product, div_n  # Return both results as a tuple


# -------------------------------
# Calling the function with positional arguments
# -------------------------------

a = 2
b = 3

# The function returns two values which are unpacked into prd and div
prd, div = productor(a, b)
print(prd)  # Output: 6 (2 * 3)
print(div)  # Output: 0.6666666666666666 (2 / 3)

# -------------------------------
# Calling the function with keyword arguments
# -------------------------------

# You can pass arguments in any order using parameter names
prd, div = productor(num_2=3, num_1=9)
print(prd)  # Output: 27 (9 * 3)
print(div)  # Output: 3.0 (9 / 3)


